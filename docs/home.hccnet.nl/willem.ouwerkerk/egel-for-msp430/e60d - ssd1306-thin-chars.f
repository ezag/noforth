: .THINROW  ( a -- )            \ Output half of 7x16 (big) character
    40 {ol  0 i2out             \ First column empty
    07 0 do
        count i2out  1+         \ Output every second bit row
    loop  drop
    0 ol} ;                     \ Last column empty

: &&EMIT    ( c -- )
    80 umin  bl -               \ Convert ASCII value
    7F x -  dup 09 < if         \ Line full?
      dup 0 &fill               \ Erase end of line
      x y 1+ >xy  dup 0 &fill   \ Erase end of next line too
      0 y 1+ >xy                \ To start of new line
    then  drop
    0E * big1 +  dup .thinrow   \ First half of big char
    x y 1+ >xy 1+ .thinrow      \ Second half of big char
    x 09 + y 1-  >xy ;          \ x & y to new char position

: &&TYPE     ( a u -- )      bounds ?do  i c@ &&emit  loop ;
: &&SPACES   ( u -- )        0 ?do  bl &&emit  loop ;

cr ." Length " chere swap - dm .  ." bytes "

: THIN       ( -- )                  \ Display thin large token set
    display-setup  &page
    s" Egel project" &&type          \ Startup message
    0A 2 >xy s" Characters" &&type   \ To line 2
    18 4 >xy s" by A.N." &&type      \ To line 4
    key drop  &page
    bl 30 bounds do  i &&emit  loop
    key drop  &page
    ch P 2F bounds do  i &&emit  loop ;

shield THIN\  freeze
